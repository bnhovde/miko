import type { NextPage } from "next";

import Header from "components/Header";
import Screen from "components/Screen";
import Head from "next/head";
import Image from "next/image";
import Main from "components/Main";
import Footer from "components/Footer";

const SpritePreview = dynamic(() => import("components/SpritePreview"), {
  ssr: false,
});

import dynamic from "next/dynamic";
import { useEffect, useMemo, useState } from "react";
import { getAll } from "utils/localStorage";
import localStorageKeys from "constants/localStorageKeys";
import { Sprite } from "types/sprite";
import Link from "next/link";
import { useRouter } from "next/router";

const Home: NextPage = () => {
  const router = useRouter();
  const [sprites, setSprites] = useState<Sprite[]>([]);
  const [legacyItemCount, setLegacyItemCount] = useState<number>(0);

  useEffect(() => {
    const items = getAll(localStorageKeys.SPRITE);
    const legacyItems = getAll(localStorageKeys.LEGACY_SPRITES);
    const sprites = items?.map((item) => JSON.parse(item)) as Sprite[];
    if (sprites) {
      setSprites(sprites);
    }
    if (legacyItems) {
      setLegacyItemCount(legacyItems.length);
    }
  }, []);

  return (
    <Screen scrolling>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Header
        action={{
          text: "My creations",
          url: "/app/my-creations",
        }}
      />

      <Main centered padded>
        <ul
          style={{
            display: "grid",
            gridTemplateColumns: "repeat(6, 1fr)",
            gap: "var(--spacing-2)",
            width: "100%",
          }}
        >
          {sprites.map((sprite) => (
            <li key={sprite.id}>
              <Link href={`/app/editor/${sprite.id}`}>
                <a>
                  <SpritePreview
                    hash={sprite.frames[0]}
                    palette={sprite.palette}
                    title={sprite.name}
                    author={{ id: "123", name: "You!" }}
                  />
                </a>
              </Link>
            </li>
          ))}
        </ul>
        <>
          {legacyItemCount > 0 && (
            <p style={{ paddingTop: "4rem" }}>
              PS: You have {legacyItemCount} legacy sprites. I am working on
              letting you import these..
            </p>
          )}
        </>
      </Main>
      <Footer
        shortcuts={[
          {
            children: "L",
            label: "Login",
            hotKeys: "l",
            onToggle: () => router.push("/app/login"),
          },
          {
            children: "âŽ",
            label: "Draw",
            hotKeys: "enter",
            onToggle: () => router.push("/app/editor"),
          },
        ]}
        action={{ text: "Draw!", url: "/app/editor" }}
      />
    </Screen>
  );
};

export default Home;
