import type { NextPage } from "next";

import Header from "components/Header";
import Screen from "components/Screen";
import Head from "next/head";
import Image from "next/image";
import Main from "components/Main";
import Footer from "components/Footer";

const SpritePreview = dynamic(() => import("components/SpritePreview"), {
  ssr: false,
});

import dynamic from "next/dynamic";
import { useEffect, useMemo, useState } from "react";
import { getAll } from "utils/localStorage";
import localStorageKeys from "constants/localStorageKeys";
import { Sprite } from "types/sprite";
import Link from "next/link";

const Home: NextPage = () => {
  const [sprites, setSprites] = useState<Sprite[]>([]);

  useEffect(() => {
    const items = getAll(localStorageKeys.SPRITE);
    const sprites = items?.map((item) => JSON.parse(item)) as Sprite[];
    if (sprites) {
      setSprites(sprites);
    }
  }, []);

  return (
    <Screen>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Header
        action={{
          text: "My creations",
          url: "/app/my-creations",
        }}
      />

      <Main centered padded>
        <ul
          style={{
            display: "flex",
            gap: "var(--spacing-2)",
            width: "100%",
          }}
        >
          {sprites.map((sprite) => (
            <li key={sprite.id} style={{ width: "20%" }}>
              <Link href={`/app/editor/${sprite.id}`}>
                <a>
                  <SpritePreview
                    hash={sprite.frames[0]}
                    title={sprite.name}
                    author={{ id: "123", name: "You!" }}
                  />
                </a>
              </Link>
            </li>
          ))}
        </ul>
      </Main>

      <Footer
        shortcuts={[
          { key: "L", label: "Login" },
          { key: "D", label: "Draw" },
        ]}
        action={{ text: "Draw!", url: "/app/editor" }}
      />
    </Screen>
  );
};

export default Home;
